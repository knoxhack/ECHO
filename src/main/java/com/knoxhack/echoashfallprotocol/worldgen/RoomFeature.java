package com.knoxhack.echoashfallprotocol.worldgen;

/**
 * Defines various architectural features that can be added to rooms.
 */
public enum RoomFeature {
    PILLAR,     // Support columns at corners
    ARCHWAY,    // Curved doorways between rooms
    BALCONY,    // Overlooking ledges
    WINDOW,     // Glass/accent openings in walls
    TRIM,       // Decorative border blocks
    PATTERN     // Floor/wall tile patterns
}
